export const aplomoCapabilityStatuses = [
  "complete",
  "partial",
  "missing",
  "planned",
] as const;

export type AplomoCapabilityStatus =
  (typeof aplomoCapabilityStatuses)[number];

export const aplomoCapabilityCriticalities = [
  "core",
  "high",
  "medium",
  "low",
] as const;

export type AplomoCapabilityCriticality =
  (typeof aplomoCapabilityCriticalities)[number];

export const aplomoCapabilityLayers = [
  "domain",
  "contracts",
  "database",
  "rls",
  "repository",
  "service",
  "audit",
  "export",
  "analytics",
  "data_quality",
  "ui",
  "ai_governance",
  "cloud_readiness",
] as const;

export type AplomoCapabilityLayer =
  (typeof aplomoCapabilityLayers)[number];

export type AplomoBackendCapability = {
  id: string;
  name: string;
  description: string;
  criticality: AplomoCapabilityCriticality;
  primaryTables: string[];
  domainFiles: string[];
  contractFiles: string[];
  uiSurfaces: string[];
  requiredPolicies: string[];
  requiredExports: string[];
  requiredAnalytics: string[];
  requiredDataQualityRules: string[];
  cloudTargets: string[];
  layerStatuses: Record<AplomoCapabilityLayer, AplomoCapabilityStatus>;
  gaps: string[];
  nextActions: string[];
};

const createLayerStatuses = (
  overrides: Partial<Record<AplomoCapabilityLayer, AplomoCapabilityStatus>>,
): Record<AplomoCapabilityLayer, AplomoCapabilityStatus> => {
  return Object.fromEntries(
    aplomoCapabilityLayers.map((layer) => [layer, overrides[layer] ?? "missing"]),
  ) as Record<AplomoCapabilityLayer, AplomoCapabilityStatus>;
};

const capability = (input: Omit<AplomoBackendCapability, "layerStatuses"> & {
  layerStatuses: Partial<Record<AplomoCapabilityLayer, AplomoCapabilityStatus>>;
}): AplomoBackendCapability => ({
  ...input,
  layerStatuses: createLayerStatuses(input.layerStatuses),
});

