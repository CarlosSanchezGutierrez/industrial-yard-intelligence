export const getAplomoBackendCapabilityById = (
  id: string,
): AplomoBackendCapability | undefined => {
  return aplomoBackendCapabilityRegistry.find((capability) => capability.id === id);
};

export const getAplomoBackendCapabilitiesByCriticality = (
  criticality: AplomoCapabilityCriticality,
): AplomoBackendCapability[] => {
  return aplomoBackendCapabilityRegistry.filter(
    (capability) => capability.criticality === criticality,
  );
};

export const getAplomoBackendCapabilitiesByLayerStatus = (
  layer: AplomoCapabilityLayer,
  status: AplomoCapabilityStatus,
): AplomoBackendCapability[] => {
  return aplomoBackendCapabilityRegistry.filter(
    (capability) => capability.layerStatuses[layer] === status,
  );
};

export const flattenAplomoBackendCapabilityLayers = () => {
  return aplomoBackendCapabilityRegistry.flatMap((capability) =>
    aplomoCapabilityLayers.map((layer) => ({
      capabilityId: capability.id,
      capabilityName: capability.name,
      criticality: capability.criticality,
      layer,
      status: capability.layerStatuses[layer],
    })),
  );
};

export const summarizeAplomoBackendCapabilityRegistry = () => {
  const flattened = flattenAplomoBackendCapabilityLayers();

  return {
    capabilityCount: aplomoBackendCapabilityRegistry.length,
    layerCount: flattened.length,
    completeLayerCount: flattened.filter((item) => item.status === "complete").length,
    partialLayerCount: flattened.filter((item) => item.status === "partial").length,
    missingLayerCount: flattened.filter((item) => item.status === "missing").length,
    plannedLayerCount: flattened.filter((item) => item.status === "planned").length,
    coreCapabilityCount: aplomoBackendCapabilityRegistry.filter(
      (capability) => capability.criticality === "core",
    ).length,
    cloudTargetCount: new Set(
      aplomoBackendCapabilityRegistry.flatMap((capability) => capability.cloudTargets),
    ).size,
  };
};
